// Helper function to find country by phone code
export const findCountryByPhoneCode = (phoneCode) => {
  return countries.find((country) => country.phoneCode === phoneCode);
};

// Helper function to filter countries by search term
export const filterCountries = (countries, searchTerm, t) => {
  if (!searchTerm) return countries;

  return countries.filter((country) =>
    t(`passenger_form.countries.${country.name}`, country.name)
      .toLowerCase()
      .includes(searchTerm.toLowerCase())
  );
};
